public class SettingItemData {
    private String settingName;
    private int settingImage;
    private String settingDescribable;

    public String getSettingName() {
        return settingName;
    }

    public void setSettingName(String settingName) {
        this.settingName = settingName;
    }

    public int getSettingImage() {
        return settingImage;
    }

    public void setSettingImage(int settingImage) {
        this.settingImage = settingImage;
    }

    public String getSettingDescribable() {
        return settingDescribable;
    }

    public void setSettingDescribable(String settingDescribable) {
        this.settingDescribable = settingDescribable;
    }
}
